# TRP5 — Flask /api/scene/refine returns 200 + refined_text
def test_trp5_flask_refine_route_200():
    from app.calliope_shell.server import create_app  # noqa: PLC0415

    app, _ = create_app()
    app.config["TESTING"] = True

    with app.test_client() as client:
        with patch("requests.post") as mock_post:
            mock_resp = MagicMock()
            mock_resp.status_code = 200
            mock_resp.json.return_value = {"result": "Aurora moved swiftly."}
            mock_resp.raise_for_status = MagicMock()
            mock_post.return_value = mock_resp

            resp = client.post(
                "/api/scene/refine",
                json={
                    "scene_text": "Aurora stood and looked at the horizon.",
                    "feedback": "rendi più breve",
                    "auto_lint": False,
                },
                content_type="application/json",
            )

    assert resp.status_code == 200
    data = resp.get_json()
    assert "refined_text" in data
    assert data["refined_text"] == "Aurora moved swiftly."


