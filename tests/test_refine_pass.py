import pytest

sys.path.insert(0, str(Path(__file__).parent.parent / "scripts"))
sys.path.insert(0, str(Path(__file__).parent.parent))


# ─── helpers ────────────────────────────────────────────────────────────────

def _make_scene_file(tmp_path: Path, body: str = "She drew her blade. The enemy fell back.") -> Path:
    p = tmp_path / "scene.md"
    p.write_text(
        f"# Scene: action_combat\n"
        f"**Tier**: cerebras_workhorse | **Provider**: cerebras | **Latency**: 100ms | **Mode**: Generated\n"
        f"\n"
        f"{body}\n"
        f"\n"
        f"---\n"
        f"_Generated by Quill of Calliope M3 — 2026-05-19T00:00:00_\n",
        encoding="utf-8",
    )
    return p


# TRP1 — --refine --feedback produces shorter output (mocked dispatch)
def test_trp1_refine_produces_shorter_output(tmp_path):
    long_body = " ".join(["Aurora stood at the cliff edge."] * 20)
    scene_file = _make_scene_file(tmp_path, long_body)
    output_file = tmp_path / "refined.md"

    short_response = "She stood. She looked. Done."

    with (
        patch("generate_scene.dispatch_to_tier", return_value=short_response),
        patch("generate_scene.route_scene", return_value={"tier": "cerebras_workhorse", "provider": "cerebras"}),
        patch("generate_scene.load_config", return_value={}),
        patch("generate_scene._publish_emotion"),
    ):
        sys.argv = [
            "generate_scene.py",
            "--refine", str(scene_file),
            "--feedback", "accorcia e semplifica",
            "--output", str(output_file),
        ]
        with pytest.raises(SystemExit) as exc:
            import generate_scene as gs  # noqa: PLC0415
            gs.main()
        assert exc.value.code == 0

    refined = output_file.read_text(encoding="utf-8")
    assert short_response in refined
    assert len(refined) < len(scene_file.read_text(encoding="utf-8")) * 2


